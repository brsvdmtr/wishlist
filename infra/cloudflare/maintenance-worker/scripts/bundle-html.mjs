// Reads the canonical maintenance HTML (the same file nginx serves at L2)
// and writes it as a TypeScript constant for the Worker bundle.
// Single source of truth: ops/maintenance/maintenance.html.
//
// Auto-run via the `prebuild` / `predev` / `predeploy` / `pretest` npm hooks,
// so `pnpm run deploy` always ships the latest HTML byte-for-byte.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..', '..', '..', '..');
const SRC_HTML = join(REPO_ROOT, 'ops', 'maintenance', 'maintenance.html');
const OUT_TS = join(__dirname, '..', 'src', 'maintenance-html.generated.ts');

const html = readFileSync(SRC_HTML, 'utf8');

const generated = `// AUTO-GENERATED by scripts/bundle-html.mjs from ops/maintenance/maintenance.html.
// DO NOT EDIT BY HAND. Re-run \`pnpm --filter @wishlist/maintenance-worker run build\`
// after editing the source HTML.

export const MAINTENANCE_HTML = ${JSON.stringify(html)};

export const MAINTENANCE_HTML_BYTES = ${html.length};
`;

mkdirSync(dirname(OUT_TS), { recursive: true });
writeFileSync(OUT_TS, generated, 'utf8');
console.log(`[bundle-html] ${SRC_HTML} -> ${OUT_TS} (${html.length} bytes)`);
